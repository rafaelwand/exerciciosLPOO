    public Contato getContato(){
        String nome = this.nome.getText();
        String email = this.email.getText();
        String endereco = this.endereco.getText();
        SimpleDateFormat sf = new SimpleDateFormat("dd/MM/yyyy");
        LocalDate dataNascimento = LocalDate.parse(data.getText(),DateTimeFormatter.ofPattern("dd/MM/yyyy"));

        Contato contato = new Contato(null,nome,email,endereco,dataNascimento);
        return contato;
    }
        
    public void setContato(Contato contato){
        //Seta os dados nos componentes
        nome.setText(contato.getNome());
        data.setText(contato.getDataNascimento().format(DateTimeFormatter.ofPattern("dd/MM/yyyy")));
        email.setText(contato.getEmail());
        endereco.setText(contato.getEndereco());        
    }


